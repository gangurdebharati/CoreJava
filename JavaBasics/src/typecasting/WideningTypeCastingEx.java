package typecasting;

public class WideningTypeCastingEx 
{
	public static void main(String[] args) 
	{
		int x=10;
		
		long y=x;
		
		float z=x;
		
		System.out.println("Before conversion of int value:"+x);
		System.out.println("After conversion into long value:"+y);
		System.out.println("Float value :"+z);

	}

}
